"""Convert GT annotations to YOLO format for training.

YOLO format: one .txt file per image, each line:
  class_id center_x center_y width height  (all normalized 0-1)

Classes:
  0 = facade
  1 = window
  2 = door
  3 = socle

Also creates dataset.yaml for ultralytics training.
"""
from __future__ import annotations

import json
import os
import shutil
import sys
from pathlib import Path

import cv2
import numpy as np

ROOT = Path(__file__).resolve().parents[2]

CLASS_MAP = {"facade": 0, "window": 1, "door": 2, "socle": 3}
CLASS_NAMES = ["facade", "window", "door", "socle"]


def bbox_to_yolo(bbox, img_w, img_h):
    """Convert [x, y, w, h] to YOLO normalized [cx, cy, w, h]."""
    x, y, w, h = bbox
    cx = (x + w / 2.0) / img_w
    cy = (y + h / 2.0) / img_h
    nw = w / img_w
    nh = h / img_h
    return cx, cy, nw, nh


def main():
    gt_dir = ROOT / "detector cantitati" / "gt"
    raw_dir = ROOT / "detector cantitati" / "raw"
    out_dir = ROOT / "data" / "yolo_facade"

    # Clean output
    if out_dir.exists():
        shutil.rmtree(out_dir)

    images_dir = out_dir / "images" / "train"
    labels_dir = out_dir / "labels" / "train"
    images_dir.mkdir(parents=True)
    labels_dir.mkdir(parents=True)

    # Also create val split (use 3 images for validation)
    val_images = out_dir / "images" / "val"
    val_labels = out_dir / "labels" / "val"
    val_images.mkdir(parents=True)
    val_labels.mkdir(parents=True)

    # Collect all GT files with their raw images
    pairs = []
    for dirpath, _, filenames in os.walk(str(gt_dir)):
        for fname in filenames:
            if not fname.endswith(".json"):
                continue
            gt_path = Path(dirpath) / fname
            with open(gt_path, encoding="utf-8") as f:
                gt = json.load(f)

            # Find matching raw image
            img_name = fname.replace(".json", ".png")
            raw_path = None
            for rdir, _, rfiles in os.walk(str(raw_dir)):
                if img_name in rfiles:
                    raw_path = Path(rdir) / img_name
                    break

            if raw_path is None or not raw_path.exists():
                continue

            has_annotations = any(
                len(gt.get(k, [])) > 0
                for k in ["facades", "windows", "doors", "socles"]
            )
            if not has_annotations:
                continue

            pairs.append((gt_path, raw_path, gt))

    print(f"Found {len(pairs)} annotated image pairs")

    # Split: last 3 for validation
    pairs.sort(key=lambda p: p[1].name)
    val_count = min(3, max(1, len(pairs) // 6))
    train_pairs = pairs[:-val_count]
    val_pairs = pairs[-val_count:]

    total_labels = 0

    for split_name, split_pairs, img_dir, lbl_dir in [
        ("train", train_pairs, images_dir, labels_dir),
        ("val", val_pairs, val_images, val_labels),
    ]:
        for gt_path, raw_path, gt in split_pairs:
            # Load image to get dimensions
            data = np.fromfile(str(raw_path), dtype=np.uint8)
            img = cv2.imdecode(data, cv2.IMREAD_COLOR)
            if img is None:
                continue
            img_h, img_w = img.shape[:2]

            # Copy image
            dst_img = img_dir / raw_path.name
            shutil.copy2(raw_path, dst_img)

            # Build YOLO labels
            lines = []
            for cat in ["facades", "windows", "doors", "socles"]:
                class_id = CLASS_MAP.get(cat.rstrip("s"), -1)
                if class_id < 0:
                    # Try singular
                    class_id = CLASS_MAP.get(cat, -1)
                if class_id < 0:
                    continue
                for item in gt.get(cat, []):
                    bbox = item.get("bbox") if isinstance(item, dict) else item
                    if not bbox or len(bbox) != 4:
                        continue
                    cx, cy, nw, nh = bbox_to_yolo(bbox, img_w, img_h)
                    # Clamp to [0, 1]
                    cx = max(0.0, min(1.0, cx))
                    cy = max(0.0, min(1.0, cy))
                    nw = max(0.001, min(1.0, nw))
                    nh = max(0.001, min(1.0, nh))
                    lines.append(f"{class_id} {cx:.6f} {cy:.6f} {nw:.6f} {nh:.6f}")
                    total_labels += 1

            lbl_path = lbl_dir / raw_path.name.replace(".png", ".txt").replace(".jpg", ".txt")
            lbl_path.write_text("\n".join(lines), encoding="utf-8")
            print(f"  {split_name}: {raw_path.name} -> {len(lines)} labels")

    # Create dataset.yaml
    yaml_content = f"""# DrawQuantPDF Facade Detection Dataset
# Auto-generated by convert_gt_to_yolo.py

path: {out_dir.as_posix()}
train: images/train
val: images/val

nc: {len(CLASS_NAMES)}
names: {CLASS_NAMES}
"""
    yaml_path = out_dir / "dataset.yaml"
    yaml_path.write_text(yaml_content, encoding="utf-8")

    print(f"\nDataset created at: {out_dir}")
    print(f"  Train: {len(train_pairs)} images")
    print(f"  Val: {len(val_pairs)} images")
    print(f"  Total labels: {total_labels}")
    print(f"  Classes: {CLASS_NAMES}")
    print(f"  Config: {yaml_path}")


if __name__ == "__main__":
    main()
